    private void fillPredmetiCmb() {

        try {
            System.out.println("SALJEN NASTAVNIKA" + nastavnik);
            List<Predmet> predmeti = controller.getListaPredmetaByNastavnik(nastavnik);
           
            cmbPredmeti.setModel(new DefaultComboBoxModel(predmeti.toArray()));
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void getPredmetCount() throws Exception {
        jTextArea1.append(nastavnik.getIme() + "\n" + nastavnik.getPrezime() + "\n");
        List<String> list = controller.getCountByTipAngazovanja(nastavnik);
        for (String s : list) {
            jTextArea1.append(s + "\n");
        }
    }
