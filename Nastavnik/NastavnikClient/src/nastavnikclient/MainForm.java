    private void cmbPredmetiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbPredmetiActionPerformed
        Predmet predmet = (Predmet) cmbPredmeti.getSelectedItem();
        try {
            List<Angazovanje> angazovanje = controller.getAllAngazovanjaByPredmet(predmet);
            model.setAngazovanja(angazovanje);
            model.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_cmbPredmetiActionPerformed

    private void btnNovoAngazovanjeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoAngazovanjeActionPerformed
        Angazovanje a = new Angazovanje();
        a.setNastavnik(nastavnik);
        System.out.println("LOGOVAN SRMAD");
        System.out.println(nastavnik);
        Predmet p = new Predmet();
        a.setPredmet(p);
        a.setTipAngazovanja("");
        model.getAngazovanja().add(a);
        model.fireTableDataChanged();
    }//GEN-LAST:event_btnNovoAngazovanjeActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            List<Angazovanje> ang = model.getAngazovanja().stream().filter(a -> a.getNastavnik().getIme().equals(nastavnik.getIme())).collect(Collectors.toList());
            
            controller.saveAngazovanje(ang);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnObrisiAngActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiAngActionPerformed

        int index = tblAngazovanja.getSelectedRow();
        if (index != -1) {
            System.out.println(model.getAngazovanja().get(index).getNastavnik().getIme());
            System.out.println(nastavnik.getIme());
            if (model.getAngazovanja().get(index).getNastavnik().getIme().equals(nastavnik.getIme())) {
                model.getAngazovanja().remove(index);
                model.fireTableDataChanged();

            } else {
                JOptionPane.showMessageDialog(this, "Ne mozete menjati angazovanje od drugih nastavnika");

            }

        }// TODO add your handling code here:
    }//GEN-LAST:event_btnObrisiAngActionPerformed

